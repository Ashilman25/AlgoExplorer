import math
import random
from collections import deque
from dataclasses import dataclass


# ---------------------------------------------------------------------------
# Lightweight benchmark parser (skips Pydantic)
# ---------------------------------------------------------------------------

@dataclass(slots = True)
class BenchmarkGraphData:
    node_ids: list[str]
    source: str | None
    target: str | None
    directed: bool
    weighted: bool
    edges: list[tuple[str, str, float]]
    coords: dict[str, tuple[float, float]] | None


def parse_benchmark_graph(raw: dict) -> BenchmarkGraphData:
    """Extract graph data from a raw input dict without Pydantic validation.

    Safe because benchmark inputs are generated by generate_graph_input().
    """
    nodes_raw = raw["nodes"]
    edges_raw = raw["edges"]

    node_ids = [str(n["id"]) for n in nodes_raw]

    edges = []
    for e in edges_raw:
        u = str(e["source"])
        v = str(e["target"])
        w = float(e["weight"]) if "weight" in e else 1.0
        edges.append((u, v, w))

    source = str(raw["source"]) if raw.get("source") is not None else None
    target = str(raw["target"]) if raw.get("target") is not None else None
    directed = raw.get("directed", False)
    weighted = raw.get("weighted", False)

    coords = None
    if nodes_raw and nodes_raw[0].get("x") is not None:
        coords = {str(n["id"]): (float(n["x"]), float(n["y"])) for n in nodes_raw}

    return BenchmarkGraphData(
        node_ids = node_ids,
        source = source,
        target = target,
        directed = directed,
        weighted = weighted,
        edges = edges,
        coords = coords,
    )


# ---------------------------------------------------------------------------
# Node / edge helpers
# ---------------------------------------------------------------------------

def _make_nodes(size, with_coords = False):
    nodes = []
    for i in range(size):
        node = {"id": str(i)}
        if with_coords:
            side = math.isqrt(size)
            node["x"] = float(i % side)
            node["y"] = float(i // side)
        nodes.append(node)
    return nodes


# ---------------------------------------------------------------------------
# Undirected topology generators
# ---------------------------------------------------------------------------

def _tree(size):
    nodes = _make_nodes(size)
    ids = [str(i) for i in range(size)]
    random.shuffle(ids)

    edges = []
    for i in range(1, size):
        j = random.randint(0, i - 1)
        u, v = ids[i], ids[j]
        edges.append({"source": u, "target": v})

    return {"nodes": nodes, "edges": edges}


def _sparse_random(size):
    result = _tree(size)
    edges = result["edges"]

    existing = set()
    for e in edges:
        pair = (min(e["source"], e["target"]), max(e["source"], e["target"]))
        existing.add(pair)

    target_edges = 2 * size
    extra_needed = target_edges - len(edges)
    ids = [str(i) for i in range(size)]
    attempts = 0
    max_attempts = extra_needed * 10

    while len(edges) < target_edges and attempts < max_attempts:
        u = random.choice(ids)
        v = random.choice(ids)
        if u == v:
            attempts += 1
            continue
        pair = (min(u, v), max(u, v))
        if pair in existing:
            attempts += 1
            continue
        existing.add(pair)
        edges.append({"source": u, "target": v})
        attempts += 1

    result["edges"] = edges
    return result


def _dense_random(size):
    result = _tree(size)
    edges = result["edges"]

    existing = set()
    for e in edges:
        pair = (min(e["source"], e["target"]), max(e["source"], e["target"]))
        existing.add(pair)

    max_edges = size * (size - 1) // 2
    target_edges = max_edges // 2

    remaining = []
    for i in range(size):
        for j in range(i + 1, size):
            pair = (str(i), str(j))
            if pair not in existing:
                remaining.append(pair)

    random.shuffle(remaining)
    needed = target_edges - len(edges)
    for pair in remaining[:needed]:
        edges.append({"source": pair[0], "target": pair[1]})

    result["edges"] = edges
    return result


def _grid(size):
    side = math.isqrt(size)
    if side * side != size:
        raise ValueError(f"Grid size {size} must be a perfect square")

    nodes = []
    for idx in range(size):
        row = idx // side
        col = idx % side
        nodes.append({"id": str(idx), "x": float(col), "y": float(row)})

    edges = []
    for idx in range(size):
        row = idx // side
        col = idx % side
        # right neighbor
        if col < side - 1:
            edges.append({"source": str(idx), "target": str(idx + 1)})
        # down neighbor
        if row < side - 1:
            edges.append({"source": str(idx), "target": str(idx + side)})

    return {"nodes": nodes, "edges": edges}


def _complete(size):
    nodes = _make_nodes(size)
    edges = []
    for i in range(size):
        for j in range(i + 1, size):
            edges.append({"source": str(i), "target": str(j)})

    return {"nodes": nodes, "edges": edges}


# ---------------------------------------------------------------------------
# DAG topology generators
# ---------------------------------------------------------------------------

def _chain(size):
    nodes = _make_nodes(size)
    edges = []
    for i in range(size - 1):
        edges.append({"source": str(i), "target": str(i + 1)})

    return {"nodes": nodes, "edges": edges}


def _sparse_dag(size):
    result = _chain(size)
    edges = result["edges"]

    existing = set()
    for e in edges:
        existing.add((e["source"], e["target"]))

    target_edges = 2 * size
    extra_needed = target_edges - len(edges)
    attempts = 0
    max_attempts = extra_needed * 10

    while len(edges) < target_edges and attempts < max_attempts:
        u = random.randint(0, size - 2)
        v = random.randint(u + 1, size - 1)
        pair = (str(u), str(v))
        if pair in existing:
            attempts += 1
            continue
        existing.add(pair)
        edges.append({"source": str(u), "target": str(v)})
        attempts += 1

    result["edges"] = edges
    return result


def _dense_dag(size):
    result = _chain(size)
    edges = result["edges"]

    existing = set()
    for e in edges:
        existing.add((e["source"], e["target"]))

    max_forward = size * (size - 1) // 2
    target_edges_total = int(max_forward * 0.3)

    remaining = []
    for i in range(size):
        for j in range(i + 1, size):
            pair = (str(i), str(j))
            if pair not in existing:
                remaining.append(pair)

    random.shuffle(remaining)
    needed = target_edges_total - len(edges)
    for pair in remaining[:needed]:
        edges.append({"source": pair[0], "target": pair[1]})

    result["edges"] = edges
    return result


def _layered_dag(size):
    num_layers = max(2, math.isqrt(size))
    nodes = _make_nodes(size)

    # distribute nodes across layers
    base_per_layer = size // num_layers
    remainder = size % num_layers

    layers = []
    idx = 0
    for layer_i in range(num_layers):
        count = base_per_layer + (1 if layer_i < remainder else 0)
        layer_nodes = [str(i) for i in range(idx, idx + count)]
        layers.append(layer_nodes)
        idx += count

    edges = []
    edge_set = set()
    for layer_i in range(len(layers) - 1):
        current_layer = layers[layer_i]
        next_layer = layers[layer_i + 1]
        for node_id in current_layer:
            num_connections = min(random.randint(1, 3), len(next_layer))
            targets = random.sample(next_layer, num_connections)
            for t in targets:
                pair = (node_id, t)
                if pair not in edge_set:
                    edge_set.add(pair)
                    edges.append({"source": node_id, "target": t})

    return {"nodes": nodes, "edges": edges}


